#include <string.h>

char	*ft_strcpy(char *dest, const char *src)
{
	int n;

	n = 0;
	while (src[n] != '\0')
	{
		dest[n] = src[n];
		n = n + 1;
	}
	dest[n] = '\0';
	return (dest);
}
